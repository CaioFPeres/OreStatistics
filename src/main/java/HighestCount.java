public class HighestCount {

    private int height;
    private int amount;
    private int biome;


    HighestCount(){

    }

    void setHeight(int height){
        this.height = height;
    }

    void setAmount(int amount){
        this.amount = amount;
    }

    public void setBiome(int biome) {
        this.biome = biome;
    }

    public int getHeight() {
        return height;
    }

    public int getAmount() {
        return amount;
    }

    public int getBiome() {
        return biome;
    }
}
